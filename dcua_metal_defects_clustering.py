def plot_additional_diagnostics(val_loader, val_labels, etas, device, model_class, save_dir='latent_visualizations'):
    """Дополнительные диагностические графики"""
    os.makedirs(save_dir, exist_ok=True)

    for eta in etas:
        eta_str = f"{eta:.2f}".replace('.', '_')
        model_path = f"pytorch_models/model_eta_{eta_str}.pth"

        if not os.path.exists(model_path):
            continue

        model = model_class(input_dim=512, z_dim=32, n_clusters=12).to(device)
        model.load_state_dict(torch.load(model_path, map_location=device, weights_only=True))
        model.eval()

        # Сбор данных
        all_gamma = []
        all_recon_errors = []
        with torch.no_grad():
            for x in val_loader:
                x = x.to(device)
                x_recon, z, gamma, _, _ = model(x)
                all_gamma.append(gamma.cpu().numpy())
                all_recon_errors.append(
                    F.mse_loss(x_recon, x, reduction='none').mean(dim=1).cpu().numpy()
                )

        all_gamma = np.vstack(all_gamma)
        all_recon_errors = np.concatenate(all_recon_errors)
        pred_labels = np.argmax(all_gamma, axis=1)
        confidences = all_gamma.max(axis=1)
        entropies = -np.sum(all_gamma * np.log(all_gamma + 1e-10), axis=1)

        fig, ax = plt.subplots(1, 1, figsize=(6, 4.5))
        ax.hist(entropies, bins=50, edgecolor='black', alpha=0.7, color='steelblue', density=True)
        ax.axvline(x=entropies.mean(), color='red', linestyle='--', linewidth=2,
                   label=f'Средняя: {entropies.mean():.3f}')
        ax.axvline(x=np.log(12), color='gray', linestyle=':', linewidth=1,
                   label=f'max = ln(12) = {np.log(12):.3f}')
        ax.set_xlabel('Энтропия H(γ)', fontsize=10)
        ax.set_ylabel('Плотность', fontsize=10)
        ax.set_title(f'Распределение энтропии (η = {eta:.2f})', fontsize=11)
        ax.legend(fontsize=8)
        ax.grid(True, alpha=0.3)
        plt.tight_layout()
        plt.savefig(f'{save_dir}/entropy_dist_eta_{eta_str}.png', dpi=300, bbox_inches='tight')
        plt.show()

        correct = (pred_labels == val_labels)
        fig, ax = plt.subplots(1, 1, figsize=(6, 4.5))

        # Биннинг по уверенности
        n_bins = 10
        bin_edges = np.linspace(0, 1, n_bins + 1)
        bin_centers = (bin_edges[:-1] + bin_edges[1:]) / 2

        accuracies = []
        counts = []
        for i in range(n_bins):
            mask = (confidences >= bin_edges[i]) & (confidences < bin_edges[i+1])
            if mask.sum() > 0:
                accuracies.append(correct[mask].mean())
                counts.append(mask.sum())
            else:
                accuracies.append(np.nan)
                counts.append(0)

        ax.errorbar(bin_centers, accuracies, xerr=0.05, fmt='o-', color='steelblue',
                    capsize=3, label='Точность в бине')
        ax.plot([0, 1], [0, 1], 'k--', alpha=0.5, label='Идеальная калибровка')
        ax.set_xlabel('Уверенность модели', fontsize=10)
        ax.set_ylabel('Точность', fontsize=10)
        ax.set_title(f'Калибровка уверенности (η = {eta:.2f})', fontsize=11)
        ax.legend(fontsize=8)
        ax.grid(True, alpha=0.3)
        ax.set_xlim(0, 1)
        ax.set_ylim(0, 1)
        plt.tight_layout()
        plt.savefig(f'{save_dir}/calibration_eta_{eta_str}.png', dpi=300, bbox_inches='tight')
        plt.show()

        fig, ax = plt.subplots(1, 1, figsize=(6, 4.5))
        cluster_recon = [all_recon_errors[pred_labels == c].mean() for c in range(12)]
        cluster_std = [all_recon_errors[pred_labels == c].std() for c in range(12)]

        ax.bar(range(12), cluster_recon, yerr=cluster_std, capsize=3,
               color=plt.cm.tab10(np.linspace(0, 1, 12)), edgecolor='black')
        ax.set_xlabel('Кластер', fontsize=10)
        ax.set_ylabel('Средняя ошибка реконструкции', fontsize=10)
        ax.set_title(f'Реконструкция по кластерам (η = {eta:.2f})', fontsize=11)
        ax.grid(True, alpha=0.3, axis='y')
        plt.tight_layout()
        plt.savefig(f'{save_dir}/recon_by_cluster_eta_{eta_str}.png', dpi=300, bbox_inches='tight')
        plt.show()

        print(f"  ✓ Доп. графики для η={eta:.2f} сохранены")

    print(f"\nДоп. диагностика завершена! Папка: {save_dir}/")


