class WorkingVaDE_Metal(nn.Module):
    def __init__(self, input_dim=512, z_dim=32, n_clusters=12):
        super().__init__()
        self.z_dim = z_dim
        self.n_clusters = n_clusters

        # Энкодер
        self.encoder = nn.Sequential(
            nn.Linear(input_dim, 500),
            nn.BatchNorm1d(500),
            nn.ReLU(),
            nn.Linear(500, 500),
            nn.BatchNorm1d(500),
            nn.ReLU(),
        )
        self.fc_mu = nn.Linear(500, z_dim)
        self.fc_logvar = nn.Linear(500, z_dim)
        self.fc_gamma = nn.Linear(500, n_clusters)

        # Декодер (восстанавливает эмбеддинги)
        self.decoder = nn.Sequential(
            nn.Linear(z_dim, 500),
            nn.BatchNorm1d(500),
            nn.ReLU(),
            nn.Linear(500, input_dim)
        )

        # GMM prior
        self.gmm_mu = nn.Parameter(torch.randn(n_clusters, z_dim) * 3.0)
        self.gmm_logvar = nn.Parameter(torch.ones(n_clusters, z_dim) * np.log(0.3))
        self.gmm_pi = nn.Parameter(torch.ones(n_clusters) / n_clusters)

    def encode(self, x, temperature=0.3):
        h = self.encoder(x)
        mu = self.fc_mu(h)
        logvar = torch.clamp(self.fc_logvar(h), min=-4, max=3)
        gamma_logits = self.fc_gamma(h)
        gamma = F.softmax(gamma_logits / temperature, dim=-1)
        return mu, logvar, gamma

    def reparameterize(self, mu, logvar):
        std = torch.exp(0.5 * logvar)
        eps = torch.randn_like(std)
        return mu + eps * std

    def decode(self, z):
        return self.decoder(z)

    def forward(self, x, temperature=0.3):
        mu, logvar, gamma = self.encode(x, temperature)
        z = self.reparameterize(mu, logvar)
        x_recon = self.decode(z)
        return x_recon, z, gamma, mu, logvar

    def compute_gmm_prob(self, z):
        B, D = z.shape
        K = self.n_clusters
        z_exp = z.unsqueeze(1)
        mu_exp = self.gmm_mu.unsqueeze(0)
        logvar_exp = self.gmm_logvar.unsqueeze(0)
        log_prob = -0.5 * (
            D * np.log(2 * np.pi) +
            logvar_exp.sum(-1) +
            ((z_exp - mu_exp) ** 2 / torch.exp(logvar_exp)).sum(-1)
        )
        log_prob = torch.log(self.gmm_pi + 1e-10) + log_prob
        return torch.logsumexp(log_prob, dim=1)

    def update_gmm(self, z, gamma):
        """
        Обновление параметров GMM на основе текущих присваиваний
        Вызывать после каждой эпохи
        """
        with torch.no_grad():
            # gamma: [batch_size, n_clusters]
            # z: [batch_size, z_dim]

            # Сумма вероятностей по каждому кластеру
            N = gamma.sum(0) + 1e-10  # [n_clusters]

            # Обновляем веса
            self.gmm_pi.data = N / N.sum()

            # Обновляем центры
            # weighted_sum: [n_clusters, z_dim]
            weighted_sum = torch.matmul(gamma.T, z)
            self.gmm_mu.data = weighted_sum / N.unsqueeze(1)

            # Обновляем дисперсии
            # Вычисляем квадрат отклонений для каждого объекта и каждого кластера
            # z: [batch_size, 1, z_dim]
            # self.gmm_mu: [1, n_clusters, z_dim]
            z_expanded = z.unsqueeze(1)  # [batch_size, 1, z_dim]
            mu_expanded = self.gmm_mu.unsqueeze(0)  # [1, n_clusters, z_dim]

            # squared_diff: [batch_size, n_clusters, z_dim]
            squared_diff = (z_expanded - mu_expanded) ** 2

            # Взвешенная сумма квадратов отклонений
            # gamma: [batch_size, n_clusters] -> [batch_size, n_clusters, 1]
            gamma_expanded = gamma.unsqueeze(-1)  # [batch_size, n_clusters, 1]

            # weighted_var: [n_clusters, z_dim]
            weighted_var = (gamma_expanded * squared_diff).sum(dim=0)

            # Обновляем logvar
            self.gmm_logvar.data = torch.log(weighted_var / N.unsqueeze(1) + 1e-6)

    def generate_prototypes(self, dataloader, device, temperature=0.3):
        """
        Генерирует прототипы кластеров, усредняя изображения,
        которые с высокой уверенностью принадлежат к каждому кластеру.
        dataloader: DataLoader, который возвращает (изображения, метки).
        """
        self.eval()
        cluster_sums = {i: None for i in range(self.n_clusters)}
        cluster_counts = {i: 0 for i in range(self.n_clusters)}
        confidence_threshold = 0.8

        print("Генерация прототипов кластеров...")
        with torch.no_grad():
            for images, _ in tqdm(dataloader, desc="Обработка батчей"):
                # 1. Извлекаем эмбеддинги с помощью ResNet
                images = images.to(device)
                embeddings = resnet(images)

                # 2. Пропускаем эмбеддинги через VaDE модель
                _, _, gamma, _, _ = self(embeddings, temperature)

                # 3. Для каждого объекта в батче
                for i in range(images.size(0)):
                    max_conf, cluster_id = torch.max(gamma[i], dim=0)
                    if max_conf.item() > confidence_threshold:
                        if cluster_sums[cluster_id.item()] is None:
                            cluster_sums[cluster_id.item()] = images[i].cpu().clone()
                        else:
                            cluster_sums[cluster_id.item()] += images[i].cpu()
                        cluster_counts[cluster_id.item()] += 1

        prototypes = {}
        for c in range(self.n_clusters):
            if cluster_counts[c] > 0:
                prototypes[c] = cluster_sums[c] / cluster_counts[c]
            else:
                prototypes[c] = None
                print(f"Предупреждение: Не найдено изображений с уверенностью > {confidence_threshold} для кластера {c}.")

        return prototypes


class WorkingTrainer:
    def __init__(self, model, device, eta=0.0):
        self.model = model
        self.device = device
        self.eta = eta

    def compute_loss(self, x, x_recon, z, gamma, mu, logvar):
        B = x.shape[0]
        recon_loss = F.mse_loss(x_recon, x, reduction='sum') / B
        log_q_z = -0.5 * torch.sum(
            logvar + (z - mu) ** 2 / torch.exp(logvar) + np.log(2 * np.pi),
            dim=1
        )
        log_p_z = self.model.compute_gmm_prob(z)
        kl_loss = torch.mean(log_q_z - log_p_z)
        expected_center = torch.matmul(gamma, self.model.gmm_mu)
        center_loss = F.mse_loss(z, expected_center, reduction='mean') * 5.0
        entropy = -torch.sum(gamma * torch.log(gamma + 1e-10), dim=1).mean()
        entropy_loss = -self.eta * entropy
        total_loss = recon_loss + 20.0 * kl_loss + center_loss + entropy_loss
        confidence = gamma.max(dim=1)[0].mean()
        return total_loss, {
            'total': total_loss.item(),
            'recon': recon_loss.item(),
            'kl': kl_loss.item(),
            'center': center_loss.item(),
            'entropy': entropy.item(),
            'entropy_loss': entropy_loss.item(),
            'confidence': confidence.item()
        }

    def train_epoch(self, train_loader, optimizer, epoch, temperature=0.3):
        self.model.train()
        epoch_loss = 0
        epoch_entropy = 0

        # Для обновления GMM
        all_z = []
        all_gamma = []

        pbar = tqdm(train_loader, desc=f'Epoch {epoch+1:3d}')
        for batch_idx, x in enumerate(pbar):
            x = x.to(self.device)
            optimizer.zero_grad()
            x_recon, z, gamma, mu, logvar = self.model(x, temperature)

            # Сохраняем для обновления GMM
            all_z.append(z.detach())
            all_gamma.append(gamma.detach())

            loss, loss_dict = self.compute_loss(x, x_recon, z, gamma, mu, logvar)
            loss.backward()
            torch.nn.utils.clip_grad_norm_(self.model.parameters(), 5.0)
            optimizer.step()

            epoch_loss += loss_dict['total']
            epoch_entropy += loss_dict['entropy']
            if batch_idx % 20 == 0:
                pbar.set_postfix({
                    'loss': f"{loss_dict['total']:.1f}",
                    'H(γ)': f"{loss_dict['entropy']:.2f}",
                    'η': f"{self.eta:.2f}"
                })

        # После обработки всех батчей обновляем GMM
        all_z = torch.cat(all_z, dim=0)
        all_gamma = torch.cat(all_gamma, dim=0)
        self.model.update_gmm(all_z, all_gamma)

        return {
            'loss': epoch_loss / len(train_loader),
            'entropy': epoch_entropy / len(train_loader)
        }

    def evaluate(self, data_loader, true_labels, temperature=0.3):
        self.model.eval()
        all_gamma = []
        with torch.no_grad():
            for x in data_loader:
                x = x.to(self.device)
                _, _, gamma, _, _ = self.model(x, temperature)
                all_gamma.append(gamma.cpu().numpy())
        all_gamma = np.vstack(all_gamma)
        pred_labels = np.argmax(all_gamma, axis=1)
        nmi = normalized_mutual_info_score(true_labels, pred_labels)
        ari = adjusted_rand_score(true_labels, pred_labels)
        entropy = -np.sum(all_gamma * np.log(all_gamma + 1e-10), axis=1).mean()
        confidence = all_gamma.max(axis=1).mean()
        return {
            'nmi': nmi,
            'ari': ari,
            'entropy': entropy,
            'confidence': confidence,
            'gamma': all_gamma,
            'pred_labels': pred_labels
        }


def pretrain_autoencoder(model, train_loader, device, epochs=3):
    model.train()
    optimizer = optim.Adam(model.parameters(), lr=1e-3)
    print("Предобучение автоэнкодера...")
    for epoch in range(epochs):
        total_loss = 0
        for x in tqdm(train_loader, desc=f"Предобучение {epoch+1}/{epochs}"):
            x = x.to(device)
            optimizer.zero_grad()
            mu, logvar, gamma = model.encode(x, temperature=1.0)
            z = model.reparameterize(mu, logvar)
            x_recon = model.decode(z)
            loss = F.mse_loss(x_recon, x)
            loss.backward()
            optimizer.step()
            total_loss += loss.item()
        print(f"Epoch {epoch+1}: средний loss = {total_loss/len(train_loader):.4f}")
    return model


def export_to_onnx(model, device, eta, save_dir='onnx_models'):
    """Экспорт модели в формат ONNX"""
    os.makedirs(save_dir, exist_ok=True)

    model.eval()

    # Создаем пример входных данных (batch_size=1, input_dim=512)
    dummy_input = torch.randn(1, 512).to(device)

    # Путь для сохранения (заменяем точку на подчеркивание для Windows)
    eta_str = f"{eta:.2f}".replace('.', '_')
    onnx_path = os.path.join(save_dir, f"model_eta_{eta_str}.onnx")

    # Экспортируем
    torch.onnx.export(
        model,
        dummy_input,
        onnx_path,
        opset_version=13,
        input_names=['input'],
        output_names=['recon', 'z', 'gamma', 'mu', 'logvar'],
        dynamic_axes={
            'input': {0: 'batch_size'},
            'recon': {0: 'batch_size'},
            'z': {0: 'batch_size'},
            'gamma': {0: 'batch_size'},
            'mu': {0: 'batch_size'},
            'logvar': {0: 'batch_size'}
        },
        verbose=False
    )
    print(f"✓ ONNX модель сохранена: {onnx_path}")
    return onnx_path

def get_cluster_interpretation(val_labels, pred_labels, class_names):
    """Автоматически сопоставляет номера кластеров с названиями дефектов"""
    from collections import Counter
    mapping = {}
    for c in range(12):
        mask = (pred_labels == c)
        if np.sum(mask) > 0:
            most_common_idx = Counter(val_labels[mask]).most_common(1)[0][0]
            mapping[c] = class_names[most_common_idx]
        else:
            mapping[c] = f"Unknown_{c}"
    return mapping

def run_experiment_for_eta(eta, device, train_loader, val_loader, val_labels, num_epochs=30):
    print(f"Эксперимент с η = {eta:.2f}")

    start_time = time.time()

    # Создаем модель
    model = WorkingVaDE_Metal(input_dim=512, z_dim=32, n_clusters=12).to(device)

    # Предобучение
    model = pretrain_autoencoder(model, train_loader, device, epochs=10)

    # Тренер
    trainer = WorkingTrainer(model, device, eta=eta)
    optimizer = optim.Adam(model.parameters(), lr=1e-4)

    history = {
        'train_loss': [],
        'train_entropy': [],
        'val_nmi': [],
        'val_ari': [],
        'val_entropy': []
    }

    for epoch in range(num_epochs):
        train_metrics = trainer.train_epoch(train_loader, optimizer, epoch, temperature=0.3)

        if epoch % 2 == 0 or epoch == num_epochs - 1:
            val_metrics = trainer.evaluate(val_loader, val_labels, temperature=0.3)
            history['train_loss'].append(train_metrics['loss'])
            history['train_entropy'].append(train_metrics['entropy'])
            history['val_nmi'].append(val_metrics['nmi'])
            history['val_ari'].append(val_metrics['ari'])
            history['val_entropy'].append(val_metrics['entropy'])

            if epoch % 5 == 0 or epoch < 3:
                print(f"Epoch {epoch+1:3d}: "
                      f"loss={train_metrics['loss']:.1f}, "
                      f"NMI={val_metrics['nmi']:.3f}, "
                      f"H(γ)={val_metrics['entropy']:.3f}")

    final_metrics = trainer.evaluate(val_loader, val_labels, temperature=0.3)
    elapsed_time = time.time() - start_time

    print(f"\nРезультаты для η={eta:.2f}:")
    print(f"  NMI: {final_metrics['nmi']:.4f}")
    print(f"  ARI: {final_metrics['ari']:.4f}")
    print(f"  H(γ): {final_metrics['entropy']:.4f}")
    print(f"  Время: {elapsed_time:.1f} сек")

    # Сохраняем PyTorch модель
    eta_str = f"{eta:.2f}".replace('.', '_')
    pt_path = f"model_eta_{eta_str}.pth"
    torch.save(model.state_dict(), pt_path)
    print(f"✓ PyTorch модель сохранена: {pt_path}")

    # Экспортируем в ONNX
    onnx_path = export_to_onnx(model, device, eta)

    return {
        'model': model,
        'history': history,
        'final_metrics': final_metrics,
        'elapsed_time': elapsed_time,
        'pt_path': pt_path,
        'onnx_path': onnx_path,
        'pred_labels': final_metrics['pred_labels']
    }

